<h1>
  The launch of
  <em>Tiyo Soga: A Literary History</em>
</h1>
<h2>Obz Books, Cape Town, August 2018</h2>

<div class="row">
<div class="content">
  <p>
    The first launch of
    <em>Tiyo Soga: A Literary History</em>
    was held at
    <a
      href="https://www.facebook.com/Obz-Books-381694475566055/"
      target="_blank">
      Obz Books in Observatory
    </a>
    on the 21 August 2018, two blocks away from the street where I grew up. This
    was a wonderful laid-back evening of friends and family, scholars and
    speeches. Wine flowed after being donated by
    <a href="https://diemersfontein.co.za" target="_blank">
      Diemersfontein Wine Estate
    </a>
    , and Dubmasta China spilled the tunes for a truly cool vibe. A great night
    was had by all and we almost completely sold out of books!
  </p>


