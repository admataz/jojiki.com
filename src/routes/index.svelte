  <div class="book-intro content">
    <h2>Tiyo Soga: A Literary History</h2>
    <p>
      <em>Tiyo Soga: A Literary History</em>
      was published in 2018. It was launched at
      <a rel="prefetch" href="/tiyo-soga-book-launch-capetown-obs-books">
        Obz Books in Cape Town in August 2018
      </a>
      , at
      <a rel="prefetch" href="/tiyo-soga-book-launch-london-soas">
        SOAS in London in November 2018
      </a>
      and at the
      <a rel="prefetch" href="/tiyo-soga-book-launch-ginsberg-steve-biko">
        Steve Biko Foundation in Ginsberg in April 2019
      </a>
      .
    </p>

    <p>
      <em>Tiyo Soga: A Literary History</em>
      is based on my PhD, carefully rewritten to include information I accessed
      after the submission of the thesis. My interest in the Reverend Tiyo Zisani
      Soga was spurred by Cliff Dikeni, the lecturer of my Xhosa I (Standard)
      Literature class at UCT, who thought that Soga should have been on the
      syllabus. Cliff described Soga as the 'next big thing' in South African
      literary theory and mentioned that our class should say we heard it first
      in Cliff's class. However, as I worked with African post-colonial and
      protest literatures in English, Xhosa and French, I found that very little
      information on Soga had been published or produced. I collected everything
      I found and kept returning to it until there was enough material and I had
      enough opinions on it for a PhD... the research for my thesis uncovered a
      wealth of latent materials in archives across South Africa and the UK,
      which show Soga as a polyglot intellectual luminary. They are included as
      appendices in this book to enable the work of future Soga scholarship.
    </p>

    <p>
      The rest, as they say, is history. Literary, South African, Southern
      African, theological and philosophical history. Soga's significance is
      global.
    </p>
