      am a
      <a href="https://www.soas.ac.uk/staff/staff118277.php" target="_blank">
        Research Associate
      </a>
      , on the 6th November 2018. We were fortunate to have Professor Emeritus
      David Killingray as a respondent and to have the great-grand-daughter of
      Rev Soga in our group. Fellow academics and missionary historians were in
