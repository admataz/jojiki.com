      <em>Tiyo Soga: A Literary History</em>
      in London was held at at the School of Oriental and African Studies,
      sponsored by the seminar group
      <a
        href="https://www.history.ac.uk/seminars/christian-missions-global-history"
        target="_blank">
        Christian Missions in Global History
      </a>
