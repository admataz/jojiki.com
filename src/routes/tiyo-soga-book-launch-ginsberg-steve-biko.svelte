<h1>
  The launch of
  <em>Tiyo Soga: A Literary History</em>
</h1>
<h2>Steve Biko Foundation, Ginsberg, South Africa, April 2019</h2>

<div class="row">
  <div class="content">
    <p>
      In February 2019, a dream came true when the
      <a href="http://www.sbf.org.za/home/" target="_blank">
        Steve Biko Foundation in Ginsberg
      </a>
      invited me to hold a special launch of
      <em>Tiyo Soga: A Literary History</em>
      at the Steve Biko Centre in Ginsberg. I was treated to a radio interview
      on the SABC, there were limitless discussions with members of the centre,
      including Kholosa Tshadana; at the launch event that evening, the audience
      was full and the book was very well received - all available copies sold
      out right there! A night I wish I could live over and over again,
      especially having my own copy of Steve Biko's
      <em>I write what I like</em>
      signed by Nkosinathi Biko.
    </p>

